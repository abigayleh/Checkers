	public void updateBoard(Move current, Move move) {
		
		//Char values of new position of piece
		int charMoveX = 3 + (4*(move.getX()-1));
		int charMoveY = 2 + (2*(move.getY()-1));
		
		//Char values of old piece position
		int charCurrentX = 3 + (4*(current.getX()-1));
		int charCurrentY = 2 + (2*(current.getY()-1));
		
		//Holders for jump pieces
		int jumpPieceX;
		int jumpPieceY;
		
		// Delete checkers that are jumped over
		int jumpCurrentX = current.getX();
		int jumpCurrentY = current.getY();
		
		// Jump over pieces
		if(Math.abs(move.getY() - current.getY()) > 1) {
			while(move.getY() != jumpCurrentY) {
				
				// Get x value
				if(jumpCurrentX > move.getX()) {
					jumpPieceX = jumpCurrentX - 1;
					jumpCurrentX = jumpPieceX - 1;
				}
				else {
					jumpPieceX = jumpCurrentX + 1;
					jumpCurrentX = jumpPieceX + 1;
				}
					
				// Get y value
				if(jumpCurrentY > move.getY()) {
					jumpPieceY = jumpCurrentY - 1;
					jumpCurrentY = jumpPieceY - 1;
				}
				else {
					jumpPieceY = jumpCurrentY + 1;
					jumpCurrentY = jumpPieceY + 1;
				}
				
				jumpPieceX = 3 + (4*(jumpPieceX-1));
				jumpPieceY = 2 + (2*(jumpPieceY-1));
				board[jumpPieceY][jumpPieceX] = ' ';
			}
		}
		
		// Become King
		if(charMoveY == 2 || charMoveY == 16)
			current.getPiece().becomeKing();
		
		// Move piece
		board[charCurrentY][charCurrentX] = ' ';
		board[charMoveY][charMoveX] = current.getPiece().getPlayer();
		
	}

